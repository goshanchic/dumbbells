package com.example.a3aaaa;

public class SensorData {
    private String timestamp;
    private int sensorId;
    private float x;
    private float y;
    private float z;

    // Getters
    public String getTimestamp() {
        return timestamp;
    }

    public int getSensorId() {
        return sensorId;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getZ() {
        return z;
    }
}

